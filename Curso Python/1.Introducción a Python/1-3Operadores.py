
# Declaración de variables
numero_a = 10
numero_b = 3

# Operadores aritméticos
suma = numero_a + numero_b  # Suma
resta = numero_a - numero_b  # Resta
multiplicacion = numero_a * numero_b  # Multiplicación
division = numero_a / numero_b  # División
division_entera = numero_a // numero_b  # División entera (devuelve la parte entera)
modulo = numero_a % numero_b  # Módulo (Devuelve el residuo de la división)
exponenciacion = numero_a ** numero_b  # Exponenciación (Eleva el primer valor a la potencia del segundo.)

# Operadores de comparación
igualdad = numero_a == numero_b  # Igualdad
desigualdad = numero_a != numero_b  # Desigualdad
mayor_que = numero_a > numero_b  # Mayor que
menor_que = numero_a < numero_b  # Menor que
mayor_igual_que = numero_a >= numero_b  # Mayor o igual que
menor_igual_que = numero_a <= numero_b  # Menor o igual que

# Operadores lógicos
condicion_1 = (numero_a > 5) and (numero_b < 7)  # AND
condicion_2 = (numero_a == 10) or (numero_b != 3)  # OR
condicion_3 = not (numero_a > 15)  # NOT

# Impresión de resultados
print("Operadores Aritméticos:")
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División Entera:", division_entera)
print("Módulo:", modulo)
print("Exponenciación:", exponenciacion)

print("\nOperadores de Comparación:")
print("Igualdad:", igualdad)
print("Desigualdad:", desigualdad)
print("Mayor que:", mayor_que)
print("Menor que:", menor_que)
print("Mayor o igual que:", mayor_igual_que)
print("Menor o igual que:", menor_igual_que)

print("\nOperadores Lógicos:")
print("AND:", condicion_1)
print("OR:", condicion_2)
print("NOT:", condicion_3)
